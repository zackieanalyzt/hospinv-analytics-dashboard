CREATE FUNCTION `ymd2dmy`(`date` char(8))
 RETURNS datetime
BEGIN
  Declare md Char(4);
  Set md=Right(date,4);
  IF md='0000' then
  Set md='0101' ; END IF ;
   
  RETURN   convert(concat(left(date,4),md),datetime);
END
