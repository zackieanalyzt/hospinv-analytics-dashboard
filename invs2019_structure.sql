        when 7 then 'กรกฎาคม'
        when 8 then 'สิงหาคม'
        when 9 then 'กันยายน'
        when 10 then 'ตุลาคม'
        when 11 then 'พฤศจิกายน'
        when 12 then 'ธันวาคม'  end);
    
    RETURN   concat( cmonth , ' ' ,convert(yearx,char)) ;


END
